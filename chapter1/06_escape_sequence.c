#include <stdio.h>

int main()
{
    //! LetsCodez: Scripting The Future!
    // escape sequence
    printf("Hello, World!\n");     //\n is an escape sequence for a new line
    printf("Hello, \"World!\"\n"); //\" is an escape sequence for double quotes
    printf("Hello, \\World!\n");   //\\ is an escape sequence for a backslash
    printf("Hello, \tWorld!\n");   //\t is an escape sequence for a tab
    printf("Hello, \bWorld!\n");   //\b is an escape sequence for a backspace
    return 0;
}